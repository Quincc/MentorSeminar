#!/bin/bash

# Запуск трёх команд sleep с разными временами в фоновом режиме
sleep 10 &
echo "Запущена команда: sleep 10 (PID: $!)"

sleep 20 &
echo "Запущена команда: sleep 20 (PID: $!)"

sleep 30 &
echo "Запущена команда: sleep 30 (PID: $!)"

# Вывод списка фоновых задач
echo "Список фоновых задач:"
jobs

# Демонстрация управления задачами
echo "Ожидание 2 секунд, чтобы продолжить управление задачами..."
sleep 2

# Перемещение задачи 1 (самая первая sleep) на передний план
echo "Перемещение первой задачи на передний план:"
fg %1

# Возвращаем задачу в фоновый режим
echo "Возвращаем задачу в фоновый режим:"
bg %1

# Проверяем оставшиеся фоновые задачи
echo "Список фоновых задач после изменений:"
jobs
